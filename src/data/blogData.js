// Helper function to get blog by slug
export const getBlogBySlug = (slug) => {
  return blogData.find(blog => blog.slug === slug);
};

// Helper function to get blog by id
export const getBlogById = (id) => {
  return blogData.find(blog => blog.id === id);
};

// Helper function to get similar blogs
export const getSimilarBlogs = (currentBlogId, limit = 3) => {
  const currentBlog = getBlogById(currentBlogId);
  if (!currentBlog) return [];
  
  return blogData
    .filter(blog => 
      blog.id !== currentBlogId && 
      (blog.category === currentBlog.category || 
       blog.tags.some(tag => currentBlog.tags.includes(tag)))
    )
    .slice(0, limit);
};

// Helper function to get all blogs
export const getAllBlogs = () => {
  return blogData;
};

// Helper function to get blogs by category
export const getBlogsByCategory = (category) => {
  return blogData.filter(blog => blog.category === category);
};

